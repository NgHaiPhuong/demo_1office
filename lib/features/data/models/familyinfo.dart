class FamilyInfo {
  final String name;
  final String phone;
  final String taxCode;
  final String idNumber;
  final String date;
  final String place;
  final String dependency;
  final String time;
  final String note;

  FamilyInfo({
    required this.name,
    required this.phone,
    required this.taxCode,
    required this.idNumber,
    required this.date,
    required this.place,
    required this.dependency,
    required this.time,
    required this.note,
  });
}